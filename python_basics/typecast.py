# The conversion of one data type into another data type is called type casting or type conversion in Python

a = "1"
b = "2"
print(a + b)  #Python do concat in case of string.

a1 = 12
b1 = 12
print(a1 + b1)

a3 = int("12")   # String should be a valid number to convert it in int data type.
b3 = 6
print(a3 + b3)

#Implicit type casting

c = 1.9
d = 8 
